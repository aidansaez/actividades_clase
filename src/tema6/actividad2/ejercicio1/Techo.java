package tema6.actividad2.ejercicio1;

public class Techo {
    private int altura;
    private int color;

    public Techo(int altura, int color) {
        this.altura = altura;
        this.color = color;
    }

    public int getAltura() {
        return altura;
    }

    public int getColor() {
        return color;
    }
}
